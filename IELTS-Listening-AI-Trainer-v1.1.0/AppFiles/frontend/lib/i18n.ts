export function translate(language: InterfaceLanguage, key: string, values: TranslationValues = {}) {
  const template = language === "zh-CN" ? zhCN[key] ?? key : key;
  return Object.entries(values).reduce(
    (result, [name, value]) => result.replaceAll(`{{${name}}}`, String(value)),
    template
  );
}

export function useI18n() {
  const { preferences } = useUserPreferences();
  const language = preferences.appearance.language;
  const t = useCallback((key: string, values?: TranslationValues) => translate(language, key, values), [language]);
  return { language, t };
}
